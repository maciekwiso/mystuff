package com.visosoft.tvshowinfo.util;

public class ShowSearchResult {
	private String name;
	private String showUpdaterId;
	private String showId;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getShowUpdaterId() {
		return showUpdaterId;
	}
	public void setShowUpdaterId(String showUpdaterId) {
		this.showUpdaterId = showUpdaterId;
	}
	public String getShowId() {
		return showId;
	}
	public void setShowId(String showId) {
		this.showId = showId;
	}
	
}
